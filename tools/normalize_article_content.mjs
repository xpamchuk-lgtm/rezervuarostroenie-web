function list(items) {
  return `<ul class="bullet-list">${items.map((item) => `<li>${escapeHtml(item)}</li>`).join("")}</ul>`;
}

function articleContent(title, topic) {
  const data = TOPICS[topic] ?? TOPICS.rvs;
  return `<section id="content">
      <h2>${escapeHtml(title)}</h2>
      <p>${escapeHtml(data.meaning)}</p>
      <h2>Что проверить инженеру</h2>
      ${list(data.checks)}
      <h2>Типовые ошибки</h2>
      ${list(data.mistakes)}
      <h2>Что зафиксировать в проекте</h2>
      <p>${escapeHtml(data.result)}</p>
    </section>`;
}

async function main() {
  const files = await walk(ROOT);
  let changed = 0;
  for (const file of files) {
    const slug = path.basename(path.dirname(file));
    const html = await fs.readFile(file, "utf8");
    const title = plain(html.match(/<h1[^>]*>([\s\S]*?)<\/h1>/i)?.[1] ?? slug);
    const topic = topicFor(slug, title);
    const next = html.replace(/<section id="content">[\s\S]*?<\/section>/, articleContent(title, topic));
    if (next !== html) {
      await fs.writeFile(file, next, "utf8");
      changed += 1;
    }
  }
  console.log(`Normalized content in ${changed} articles`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
