import fs from "node:fs/promises";
import path from "node:path";

const ROOT = path.resolve("frontend/public/articles");

function escapeHtml(value) {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function plain(value) {
  return value.replace(/<[^>]+>/g, " ").replace(/\s+/g, " ").trim();
}

async function walk(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) files.push(...await walk(full));
    if (entry.isFile() && entry.name === "index.html") files.push(full);
  }
  return files;
}

function topicFor(slug, title) {
  const text = `${slug} ${title}`.toLowerCase();
  if (/gost|sp|norm|норм|гост|сп\d|тр тс|фнп|17032/.test(text)) return "norms";
  if (/sug|суг|propan|butan|пропан|бутан|сосуд|давлен|избыточ/.test(text)) return "sug";
  if (/rgs|gorizontal|горизонтальн|обечайк|диафрагм|седл|хомут|всплыти|овальн|эллиптич/.test(text)) return "rgs";
  if (/dnishch|okray|днищ|окрайк|утор|основан|фундамент|осадк|грунт|разуклон/.test(text)) return "bottom";
  if (/stenk|tolshch|poyas|стенк|толщин|пояс|сталь|09г2с|ст3/.test(text)) return "wall";
  if (/krovl|roof|кровл|настил|ребр|уклон|стойк/.test(text)) return "roof";
  if (/sneg|snow|снег|снегов/.test(text)) return "snow";
  if (/vetr|wind|ветр|опрокид|анкер|устойчив/.test(text)) return "wind";
  if (/seysm|сейсм|импульсив|конвектив|гидродинами/.test(text)) return "seismic";
  if (/svark|shov|монтаж|свар|шов|рулон|полист|транспорт|серповид|разбеж/.test(text)) return "assembly";
  if (/lyuk|patrub|л[юу]к|лаз|патруб|врезк|лестниц|площадк|молни|арматур/.test(text)) return "details";
  if (/korroz|корроз|покрыт|акз|припуск/.test(text)) return "corrosion";
  if (/ispyt|kontrol|laborator|гидроиспыт|испыт|контрол|лаборатор|нк|узи|рк|вик|диагност/.test(text)) return "tests";
  if (/temperatur|obogrev|teplo|температур|обогрев|теплоизоляц|деформац/.test(text)) return "temperature";
  if (/tz|тз|задан|ошиб/.test(text)) return "brief";
  return "rvs";
}

