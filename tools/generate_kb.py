def compute_example(cluster):
    ex = cluster['example']
    D = ex['D']
    H = ex['H']
    rho = ex['rho']
    V = math.pi * D * D * H / 4
    p = rho * 9.81 * H / 1000 / 1000  # MPa roughly
    A = math.pi * D * D / 4
    G = rho * V / 1000
    q = (rho * V * 9.81) / A / 1000
    return {
        'D': D,
        'H': H,
        'rho': rho,
        'V': V,
        'p': p,
        'A': A,
        'G': G,
        'q': q,
    }


