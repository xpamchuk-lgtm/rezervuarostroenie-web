def clean_old_kb():
    if KB_ROOT.exists():
        for path in sorted(KB_ROOT.rglob('*'), reverse=True):
            if path.is_file() or path.is_symlink():
                path.unlink()
            elif path.is_dir():
                try:
                    path.rmdir()
                except OSError:
                    pass
        try:
            KB_ROOT.rmdir()
        except OSError:
            pass
    KB_ROOT.mkdir(parents=True, exist_ok=True)


