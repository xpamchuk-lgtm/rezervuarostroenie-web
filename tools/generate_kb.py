def related_pages(page_slug: str, group: str):
    same = [p for p in pages if p['group'] == group and p['slug'] != page_slug]
    same = same[:3]
    anchor = next(p for p in pages if p['group'] == group and p['anchor'])
    general_anchor = next(p for p in pages if p['slug'] == 'raschet-rvs')
    result = []
    if page_slug != anchor['slug']:
        result.append(anchor)
    if page_slug != general_anchor['slug'] and general_anchor['slug'] != anchor['slug']:
        result.append(general_anchor)
    result.extend(same)
    uniq = []
    seen = {page_slug}
    for p in result:
        if p['slug'] not in seen:
            uniq.append(p)
            seen.add(p['slug'])
        if len(uniq) >= 5:
            break
    return uniq


