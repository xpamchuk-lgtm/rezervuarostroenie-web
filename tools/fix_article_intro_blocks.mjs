function renderIntro(topic) {
  const paragraphs = INTROS[topic] ?? INTROS.rvs;
  return `<section class="editorial-section">
    <h2>Инженерный смысл материала</h2>
    ${paragraphs.map((item) => `<p>${escapeHtml(item)}</p>`).join("\n    ")}
  </section>`;
}

async function main() {
  const files = await walk(ROOT);
  let changed = 0;

  for (const file of files) {
    const slug = path.basename(path.dirname(file));
    const html = await fs.readFile(file, "utf8");
    const topic = topicFor(slug, html);
    const next = html.replace(
      /<section class="editorial-section">\s*<h2>Инженерный смысл материала<\/h2>[\s\S]*?<\/section>/,
      renderIntro(topic),
    );

    if (next !== html) {
      await fs.writeFile(file, next, "utf8");
      changed += 1;
    }
  }

  console.log(`Updated intro blocks in ${changed} articles`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
